#include <unistd.h>
#include "h_list.h"

void my_show_tab(char **tab)
{
  int	i;

  i = 0;
  while (tab[i] != NULL)
    {
      my_putstr(tab[i]);
      write(1, "\n", 1);
      i++;
    }
}
